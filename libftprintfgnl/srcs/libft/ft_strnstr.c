#include "libft.h"

char	*ft_strnstr(const char *big, const char *little, size_t len)
{
	unsigned int	i;
	int				j;

	i = 0;
	j = 0;
	if (!*little)
		return ((char *)big);
	while (big[i] && i < len)
	{
		j = 0;
		if (big[i] == little[j])
		{
			while (big[i + j] == little[j] && i + j < len
				&& big[i + j] && little[j++])
			{
				if (!little[j])
					return (((char *)big) + i);
			}
		}
		i++;
	}
	return (0);
}
